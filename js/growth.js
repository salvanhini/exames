    // Calcular idade em meses a partir da data de nascimento
    idadeEmMeses(dataNascimento, dataMedicao) {
        const nasc = new Date(dataNascimento);
        const med = new Date(dataMedicao);
        const diffMs = med - nasc;
        return diffMs / (1000 * 60 * 60 * 24 * 30.4375);
    },

    // Encontrar percentil para um valor
    calcularPercentil(valor, tabela, meses) {
        if (!tabela || tabela.length === 0) return null;
        
        // Encontrar faixa etária
        let linha = tabela[0];
        for (let i = 0; i < tabela.length; i++) {
            if (tabela[i][0] <= meses) linha = tabela[i];
        }

        const [_, p3, p15, p50, p85, p97] = linha;
        const refs = [
            { nome: 'P3', valor: p3 },
            { nome: 'P15', valor: p15 },
            { nome: 'P50', valor: p50 },
            { nome: 'P85', valor: p85 },
            { nome: 'P97', valor: p97 }
        ];

        if (valor < p3) return { percentil: '<3', cor: '#ef4444' };
        if (valor < p15) return { percentil: '3', cor: '#f59e0b' };
        if (valor < p50) return { percentil: '15', cor: '#10b981' };
        if (valor < p85) return { percentil: '50', cor: '#10b981' };
        if (valor < p97) return { percentil: '85', cor: '#f59e0b' };
        return { percentil: '>97', cor: '#ef4444' };
    },

    // Obter dados de referência para o gráfico
